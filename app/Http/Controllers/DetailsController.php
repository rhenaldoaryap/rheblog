     * @return \Illuminate\Http\Response
     */
    public function index($slug)
    {
        $post = Post::with('category', 'user')->where('slug', $slug)->first();
        $popularPost = Post::orderByViews()->take(10)->get();
        $relatedPost = Post::orderBy('category_id', 'DESC')->inRandomOrder()->take(6)->get();
        views($post)->record();

        if($post){
            return view('pages.details', compact('post', 'popularPost', 'relatedPost'));
        } else {
            return redirect('/');
        }
    }

